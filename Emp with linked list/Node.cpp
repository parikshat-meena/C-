#include "Node.h"

Node::Node(Emp d)
{
	this->data = d;
	this->next = NULL;	
}
/////////////////////////////////////////////////
void Node::setData(Emp d)
{
	this->data = d;
}
/////////////////////////////////////////////////
void Node::setNext(Node*nt)
{
	this->next = nt;
}
/////////////////////////////////////////////////	
Emp Node:: getData()
{
	return this->data;
}
/////////////////////////////////////////////////	
Node* Node:: getNext()
{
	return this->next;
}
