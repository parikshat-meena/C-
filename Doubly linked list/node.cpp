#include"node.h"

Node ::	Node(int d)
{
	Data = d;
	Prev = NULL;
	Next = NULL;
}
////////////////////////////////////////////////////////////
void Node::setNext(Node* nt)
{
	this->Next = nt;
}
////////////////////////////////////////////////////////////
void Node::setPrev(Node* pt)
{
	this->Prev = pt;
}
////////////////////////////////////////////////////////////
void Node::setData(int d)
{
	this->Data = d;
}
////////////////////////////////////////////////////////////
int Node:: getData()
{
	return this->Data;
}
////////////////////////////////////////////////////////////
Node* Node::getNext()
{
	return this->Next;
}
////////////////////////////////////////////////////////////
Node* Node::getPrev()
{
	return this->Prev;
}
